package r01e01;

public class ValoresVariables {

	public static void main(String[] args) {
		
		int valor1 =256;		
		int valor2 =398;
		//creamos las dos variable
		
		System.out.print("La suma de las dos variables es --> ");
		System.out.println(valor1 + valor2 );
		
		System.out.print("La resta de las dos variables es --> ");
		System.out.println(valor1 - valor2);
		
		System.out.print("El producto de las dos variables es --> ");
		System.out.println(valor1 * valor2);
		
		System.out.print("El cociente de las dos variables es --> ");
		System.out.println(valor1 / valor2);
		
		System.out.print("El modulo de las dos variables es --> ");
		System.out.println((valor1*valor1) + (valor2*valor2));
	}

}
